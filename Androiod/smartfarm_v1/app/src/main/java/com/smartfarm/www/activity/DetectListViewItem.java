package com.smartfarm.www.activity;

public class DetectListViewItem {
    private String detect_time;
    private String detect_title;
    private String detect_content;

    public String getTime() {
        return detect_time;
    }

    public void setTime(String detect_time) {
        this.detect_time = detect_time;
    }

    public String getTitle() {
        return detect_title;
    }

    public void setTitle(String detect_title) {
        this.detect_title = detect_title;
    }

    public String getContent() {
        return detect_content;
    }

    public void setContent(String detect_content) {
        this.detect_content = detect_content;
    }
}
